
package holamundo;

public class Persona {
    private int edad;
    private int peso;
    private int altura;

    public Persona() {
        this.edad = 0;
        this.peso = 0;
        this.altura = 0;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }  
}
